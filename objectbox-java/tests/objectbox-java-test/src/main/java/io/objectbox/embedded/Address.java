package io.objectbox.embedded;

/**
 * Embeddable value object for testing {@code @Embedded} support.
 * <p>
 * This is NOT an {@code @Entity} â€” it has no ID and is not persisted as a standalone table.
 * Its fields are flattened into the parent entity's table.
 */
public class Address {

    private String street;
    private String city;
    private int zip;

    /** Required no-arg constructor. */
    public Address() {
    }

    public Address(String street, String city, int zip) {
        this.street = street;
        this.city = city;
        this.zip = zip;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public int getZip() {
        return zip;
    }

    public void setZip(int zip) {
        this.zip = zip;
    }
}
